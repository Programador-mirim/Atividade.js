let idade_do_usuario = 18
if (idade_do_usuario <= 12) {
    console.log("Crianca")
}
else if (idade_do_usuario >= 13 && idade_do_usuario <= 17) {
    console.log("Adolescente")
}
else if (idade_do_usuario >= 18 && idade_do_usuario <= 59) {
    console.log("Adulto")
}
else {
    console.log("Idoso")
}